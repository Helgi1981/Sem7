// Задайте произвольный массив. Выведете его элементы, начиная с конца. 
// Использовать рекурсию, не использовать циклы.

// Пример:
// [1, 2, 5, 0, 10, 34] => 34 10 0 5 2 1


// Рекурсивная функция для вывода элементов массива в обратном порядке
void PrintArrayReverse(int[] array, int index)
{
    // Базовый случай: если индекс меньше нуля, выходим из рекурсии
    if (index < 0) return;

    // Вывод текущего элемента
    Console.Write(array[index] + " ");

    // Рекурсивный вызов для следующего элемента
    PrintArrayReverse(array, index - 1);
}

// Ввод массива
int[] array = { 1, 2, 5, 0, 10, 34 };

// Вызов рекурсивной функции, начиная с последнего элемента массива
PrintArrayReverse(array, array.Length - 1);

// Переход на новую строку
Console.WriteLine();